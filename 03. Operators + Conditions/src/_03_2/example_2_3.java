package _03_2;

public class example_2_3 {
    // Câu điều kiện rẽ nhánh “if…else”
    public static void main(String[] args) {
        int a = 1;

        if (a > 0) {
            System.out.println("YES"); // YES vì a = 1 > 0
        } else {
            System.out.println("NO");
        }
    }
}
